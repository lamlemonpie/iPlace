    public function buscar(Ambito $ambito)
    {


      $eventos = Evento::with('eventos_ambito.ambito')->whereHas('eventos_ambito', function($q) use ($ambito)
      {
          $q->where('id_ambito', $ambito->id);

      })->get();
      //$eventos = $eventos->toArray();
      //dd($eventos);
      return view('eventos.mostrar',['eventos'=>$eventos,'categoria'=>$ambito]);
    }


